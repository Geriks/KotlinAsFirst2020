 */
fun hasDifferentDigits(n: Int): Boolean {
    var ans = false
    var x = n / 10
    var check = n % 10
    while (x != 0) {
        if (check == x % 10) ans = false
        else {
            ans = true
            break
        }
        check = x % 10
        x /= 10
    }
    return ans
}

/**
 * Средняя (4 балла)
